import re
from typing import Dict, List, Optional, Union

from tqdm import tqdm

from lmms_eval import utils
from lmms_eval.api.instance import Instance
from lmms_eval.api.registry import register_model
from lmms_eval.models.simple.sttv_implicit_grounding_vllm import STTVImplicitGroundingVLLM

try:
    from vllm import LLM, SamplingParams
except ImportError:
    LLM = None
    SamplingParams = None


@register_model("sttv_implicit_grounding_base_verifier_vllm")
class STTVImplicitGroundingBaseVerifierVLLM(STTVImplicitGroundingVLLM):
    """
    Eval-time implicit-grounding flow with base model as logic verifier:
    query -> <reason><answer> -> base-model edits -> rewritten <reason><answer>.

    Main generation model can be a finetuned checkpoint while verifier edits are
    generated by a separate base model.
    """

    def __init__(
        self,
        model: str = "Qwen/Qwen3-VL-4B-Instruct",
        tensor_parallel_size: int = 1,
        data_parallel_size: int = 1,
        gpu_memory_utilization: float = 0.55,
        batch_size: Optional[Union[int, str]] = 1,
        depth: Union[bool, str, int, float] = False,
        prompt_path: Optional[str] = None,
        instruction_mode: str = "box",
        max_image_side: int = 768,
        generation_chunk_max_new_tokens: int = 768,
        logic_verifier_rounds: int = 1,
        logic_verifier_max_new_tokens: int = 128,
        trust_remote_code: Optional[bool] = True,
        chat_template: Optional[str] = None,
        min_image_pixels: int = 28,
        seed: int = 1,
        disable_log_stats: bool = False,
        verifier_model: str = "Qwen/Qwen3-VL-4B-Instruct",
        verifier_tensor_parallel_size: Optional[int] = None,
        verifier_gpu_memory_utilization: float = 0.35,
        verifier_trust_remote_code: Optional[bool] = None,
        verifier_chat_template: Optional[str] = None,
        verifier_seed: Optional[int] = None,
        verifier_disable_log_stats: Optional[bool] = None,
        verifier_max_model_len: Optional[int] = 8192,
        verifier_dtype: Optional[str] = None,
        logic_verifier_answer_max_chars: int = 4000,
        **kwargs,
    ) -> None:
        super().__init__(
            model=model,
            tensor_parallel_size=tensor_parallel_size,
            data_parallel_size=data_parallel_size,
            gpu_memory_utilization=gpu_memory_utilization,
            batch_size=batch_size,
            depth=depth,
            prompt_path=prompt_path,
            instruction_mode=instruction_mode,
            max_image_side=max_image_side,
            generation_chunk_max_new_tokens=generation_chunk_max_new_tokens,
            logic_verifier_rounds=logic_verifier_rounds,
            logic_verifier_max_new_tokens=logic_verifier_max_new_tokens,
            trust_remote_code=trust_remote_code,
            chat_template=chat_template,
            min_image_pixels=min_image_pixels,
            seed=seed,
            disable_log_stats=disable_log_stats,
            **kwargs,
        )
        if LLM is None or SamplingParams is None:
            raise ImportError("vllm is not installed. Please install vllm to use sttv_implicit_grounding_base_verifier_vllm.")

        verifier_tp = int(verifier_tensor_parallel_size or tensor_parallel_size)
        verifier_seed_value = int(verifier_seed if verifier_seed is not None else (seed + 17))
        verifier_trust_remote_code_value = (
            trust_remote_code if verifier_trust_remote_code is None else bool(verifier_trust_remote_code)
        )
        verifier_disable_log_stats_value = (
            disable_log_stats if verifier_disable_log_stats is None else bool(verifier_disable_log_stats)
        )
        self.verifier_chat_template = self._load_chat_template(verifier_chat_template) if verifier_chat_template else self.chat_template
        self.verifier_model_name = str(verifier_model)

        verifier_kwargs: Dict[str, object] = {}
        if verifier_max_model_len is not None:
            verifier_kwargs["max_model_len"] = int(verifier_max_model_len)
        if verifier_dtype is not None and str(verifier_dtype).strip():
            verifier_kwargs["dtype"] = str(verifier_dtype).strip()

        self.verifier_client = LLM(
            model=self.verifier_model_name,
            tensor_parallel_size=verifier_tp,
            gpu_memory_utilization=float(verifier_gpu_memory_utilization),
            trust_remote_code=verifier_trust_remote_code_value,
            disable_log_stats=verifier_disable_log_stats_value,
            seed=verifier_seed_value,
            **verifier_kwargs,
        )
        self.logic_verifier_answer_max_chars = max(0, int(logic_verifier_answer_max_chars))

    def _truncate_answer_for_logic_verifier(self, answer_output: str) -> tuple[str, bool]:
        text = str(answer_output or "").strip()
        limit = self.logic_verifier_answer_max_chars
        if limit <= 0 or len(text) <= limit:
            return text, False
        marker = "\n...[truncated for verifier]...\n"
        keep_head = max(0, int(limit * 0.7))
        keep_tail = max(0, limit - keep_head - len(marker))
        if keep_tail <= 0:
            truncated = text[: max(0, limit - len(marker))] + marker
        else:
            truncated = text[:keep_head] + marker + text[-keep_tail:]
        return truncated, True

    def _generate_once_with_verifier_client(
        self,
        messages: List[Dict[str, object]],
        gen_kwargs: Dict[str, object],
        stop_sequences: Optional[List[str]] = None,
        max_new_tokens: int = 256,
    ) -> tuple[str, int]:
        del gen_kwargs
        params = self._build_sampling_params(
            max_tokens=max_new_tokens,
            stop_sequences=stop_sequences,
            use_verifier_sampling=False,
        )
        sampling_params = SamplingParams(**params)

        if self.verifier_chat_template is not None:
            response = self.verifier_client.chat(
                sampling_params=sampling_params,
                messages=[messages],
                chat_template=self.verifier_chat_template,
            )
        else:
            response = self.verifier_client.chat(sampling_params=sampling_params, messages=[messages])

        if not response:
            return "", 0
        output = response[0].outputs[0]
        decoded = output.text
        token_count = len(getattr(output, "token_ids", []) or [])
        if stop_sequences:
            stop_index = None
            stop_token = None
            for sequence in stop_sequences:
                idx = decoded.find(sequence)
                if idx != -1 and (stop_index is None or idx < stop_index):
                    stop_index = idx
                    stop_token = sequence
            if stop_index is not None and stop_token is not None:
                decoded = decoded[: stop_index + len(stop_token)]
            else:
                stop_reason = getattr(output, "stop_reason", None)
                if isinstance(stop_reason, str) and stop_reason in stop_sequences:
                    decoded = f"{decoded}{stop_reason}"
        return decoded, token_count

    def generate_until(self, requests: List[Instance]) -> List[str]:
        res = []

        def _collate(x):
            return -len(x[0]), x[0]

        pbar = tqdm(total=len(requests), disable=(self.rank != 0), desc="Model Responding")
        re_ords = utils.Collator([reg.args for reg in requests], _collate, grouping=True)
        chunks = re_ords.get_batched(n=self.batch_size, batch_fn=None)
        self.last_generation_metadata = None
        all_generation_metadata: List[Dict[str, object]] = []
        for chunk in chunks:
            contexts, all_gen_kwargs, doc_to_visual, doc_id, task, split = zip(*chunk)
            if isinstance(contexts, tuple):
                contexts = list(contexts)

            for i, context in enumerate(contexts):
                sample_task = task[i]
                sample_split = split[i]
                sample_doc_id = doc_id[i]
                sample_doc_to_visual = doc_to_visual[i]
                sample_visual = sample_doc_to_visual(self.task_dict[sample_task][sample_split][sample_doc_id])
                sample_gen_kwargs = dict(all_gen_kwargs[i])

                if "<image" in context:
                    context = re.sub(r"<image\s*\d+>", "", context)
                    context = context.replace("<image>", "")

                visuals = sample_visual if isinstance(sample_visual, list) else [sample_visual]
                visuals = [item for item in visuals if item is not None]

                initial_prompt = self._build_initial_answer_prompt(context)
                initial_messages = self._build_messages(initial_prompt, visuals)
                current_answer_output, _ = self._generate_once(
                    initial_messages,
                    sample_gen_kwargs,
                    stop_sequences=["</answer>"],
                    max_new_tokens=max(1, self.generation_chunk_max_new_tokens),
                )
                generation_metadata: Dict[str, object] = {
                    "requested_logic_verifier_rounds": int(self.logic_verifier_rounds),
                    "performed_logic_verifier_rounds": 0,
                    "initial_answer_draft": current_answer_output,
                    "logic_verifier_rounds": [],
                    "logic_feedback_source": "base_model_verifier",
                    "logic_feedback_verifier_model": self.verifier_model_name,
                }
                logic_round_records = generation_metadata["logic_verifier_rounds"]
                assert isinstance(logic_round_records, list)

                for round_index in range(1, self.logic_verifier_rounds + 1):
                    round_record: Dict[str, object] = {
                        "round_index": round_index,
                        "answer_before_feedback": current_answer_output,
                        "feedback_source": "base_model_verifier",
                        "feedback_model": self.verifier_model_name,
                    }
                    verifier_answer_input, answer_was_truncated = self._truncate_answer_for_logic_verifier(current_answer_output)
                    round_record["logic_verifier_input_answer_truncated"] = bool(answer_was_truncated)
                    round_record["logic_verifier_input_answer_char_len"] = len(str(current_answer_output or ""))
                    round_record["logic_verifier_input_answer_char_len_used"] = len(verifier_answer_input)
                    logic_prompt = self._build_logic_self_verifier_prompt(context, verifier_answer_input)
                    logic_messages = self._build_messages(logic_prompt, visuals)
                    logic_output, _ = self._generate_once_with_verifier_client(
                        logic_messages,
                        sample_gen_kwargs,
                        max_new_tokens=max(1, self.logic_verifier_max_new_tokens),
                    )
                    round_record["self_verifier_feedback_raw"] = logic_output
                    logic_feedback, logic_parse_valid = self._parse_logic_step_edits_optional(
                        logic_output, current_answer_output
                    )
                    round_record["self_verifier_feedback_parse_valid"] = bool(logic_parse_valid)
                    round_record["self_verifier_feedback_parsed"] = logic_feedback
                    fallback_used = False
                    if (not logic_parse_valid) or (not str(logic_feedback or "").strip()):
                        logic_feedback = "No valid self-verifier feedback was produced. Re-emit the current answer unchanged."
                        fallback_used = True
                    round_record["self_verifier_feedback_fallback_used"] = fallback_used

                    rewrite_prompt = self._build_answer_rewrite_prompt(context, current_answer_output, logic_feedback)
                    rewrite_messages = self._build_messages(rewrite_prompt, visuals)
                    current_answer_output, _ = self._generate_once(
                        rewrite_messages,
                        sample_gen_kwargs,
                        stop_sequences=["</answer>"],
                        max_new_tokens=max(1, self.generation_chunk_max_new_tokens),
                    )
                    round_record["answer_after_rewrite"] = current_answer_output
                    logic_round_records.append(round_record)

                generation_metadata["performed_logic_verifier_rounds"] = len(logic_round_records)
                generation_metadata["final_answer_output"] = current_answer_output

                res.append(current_answer_output)
                all_generation_metadata.append(generation_metadata)
                self.cache_hook.add_partial("generate_until", (context, sample_gen_kwargs), current_answer_output)
                pbar.update(1)
                self._cleanup_after_sample()

        res = re_ords.get_original(res)
        self.last_generation_metadata = re_ords.get_original(all_generation_metadata)
        pbar.close()
        return res
